package com.maigangle.gyl.facade.enums;

/**  
 * 货物来源枚举
 * @author zlh
 * @since 2017年12月2日 上午10:32:20 
 * @version 1.0 
 */
public enum GoodsSourceEnum {
	
	GANG_CHANG_ZI_TI("A", "钢厂自提"),
	KU_CUN_HUO_WU("B", "库存货物"),
	CANG_DAN_ZHI_YA("C", "仓单质押");
	
	private String type;
	
	private String name;
	
	private GoodsSourceEnum(String type, String name) {
		this.type = type;
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return this.type;
	}
	
	
}
