package com.maigangle.gyl.facade.enums;

public enum RemitTypeEnum {
	CAI_GOU_DA_KUAN("A","采购打款"),
	RONG_ZI_DA_KUAN("B","融资打款");
	
	private String type;
	
	private String name;
	
	private RemitTypeEnum(String type, String name) {
		this.type = type;
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return this.type;
	}
}
