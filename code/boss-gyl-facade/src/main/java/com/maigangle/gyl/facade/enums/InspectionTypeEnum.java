public enum InspectionTypeEnum {
	YI_YAN_HUO("A","已验货"),
	YAN_HUO_ZHONG("B","验货中"),
	WEI_YAN_HUO("C","未验货");
	
	
	
	
	private String type;
	
	private String name;
	
	private InspectionTypeEnum(String type, String name) {
		this.type = type;
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return this.type;
	}
}
