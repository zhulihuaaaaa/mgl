public enum InOrNotStockEnum {
	RUKUHUOWU("A","仓库中"),
	CHUKUHUOWU("B","已出库");
	private String type;
	
	private String name;
	
	private InOrNotStockEnum(String type, String name) {
		this.type = type;
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return this.type;
	}
}
